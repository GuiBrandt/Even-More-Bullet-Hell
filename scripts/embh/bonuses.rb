#==============================================================================
# LifeBonus
#------------------------------------------------------------------------------
# Bônus de vida
#==============================================================================
class LifeBonus < Bonus
	#--------------------------------------------------------------------------
	# Cor do objeto
	#--------------------------------------------------------------------------
	def color
		return $player.color.inverse
	end
	#--------------------------------------------------------------------------
	# Aplica o bônus a um atirador
	#--------------------------------------------------------------------------
	def apply! shooter
		shooter.heal
	end
end